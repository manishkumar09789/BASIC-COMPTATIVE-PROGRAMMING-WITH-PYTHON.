Percentage=float(input("Enter The Percentage : "))
print(Percentage, " %")
if(Percentage>=90):
    print("Grade A")
elif(Percentage>=80 and Percentage<90):
    print("Grade  B")    
elif(Percentage>=70 and Percentage<80):  
    print("Grade C")
elif(Percentage>=60 and Percentage,70): 
    print("Grade D") 
else:
    print("Failed")      